// Builds the full PWA/favicon icon set from the real Segmax logo (public/segmaxlogod.png).
// Usage: node scripts/generate-pwa-icons.mjs
import sharp from "sharp";
import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

const SOURCE_LOGO = path.join(root, "public", "segmaxlogod.png");
const BRAND_NAVY = { r: 25, g: 19, b: 89, alpha: 1 }; // #191359, sampled from the logo

// The source file has off-white canvas margin around the navy badge, and a
// wordmark below it that's illegible at icon sizes — crop to just the navy
// badge (bounding box found by scanning for navy pixels) and re-square it.
const BADGE_CROP = { left: 300, top: 62, width: 646, height: 826 };

async function buildSquareMaster() {
  const side = Math.max(BADGE_CROP.width, BADGE_CROP.height);
  const badge = await sharp(SOURCE_LOGO).extract(BADGE_CROP).toBuffer();
  return sharp({
    create: { width: side, height: side, channels: 4, background: BRAND_NAVY },
  })
    .composite([{ input: badge, left: Math.round((side - BADGE_CROP.width) / 2), top: 0 }])
    .png()
    .toBuffer();
}

/** Extra navy padding so OS-applied circular/squircle masks don't clip the artwork. */
async function buildMaskableMaster(squareMaster) {
  const meta = await sharp(squareMaster).metadata();
  const padded = Math.round(meta.width * 0.18);
  const side = meta.width + padded * 2;
  return sharp({
    create: { width: side, height: side, channels: 4, background: BRAND_NAVY },
  })
    .composite([{ input: squareMaster, left: padded, top: padded }])
    .png()
    .toBuffer();
}

async function renderPng(source, outPath, size) {
  await sharp(source).resize(size, size).png().toFile(outPath);
  console.log("wrote", path.relative(root, outPath));
}

async function main() {
  const iconsDir = path.join(root, "public", "icons");
  await mkdir(iconsDir, { recursive: true });

  const squareMaster = await buildSquareMaster();
  const maskableMaster = await buildMaskableMaster(squareMaster);

  await renderPng(squareMaster, path.join(iconsDir, "icon-192.png"), 192);
  await renderPng(squareMaster, path.join(iconsDir, "icon-512.png"), 512);
  await renderPng(maskableMaster, path.join(iconsDir, "icon-maskable-512.png"), 512);

  // Next.js static metadata file conventions (auto-linked, no manual <link> needed).
  await renderPng(squareMaster, path.join(root, "src", "app", "apple-icon.png"), 180);
  await renderPng(squareMaster, path.join(root, "src", "app", "icon.png"), 256);

  // Favicon fallback.
  await renderPng(squareMaster, path.join(iconsDir, "favicon-32.png"), 32);

  await writeFile(
    path.join(iconsDir, "README.txt"),
    "Generated by scripts/generate-pwa-icons.mjs from public/segmaxlogod.png — re-run after the logo changes.\n"
  );
}

main();
