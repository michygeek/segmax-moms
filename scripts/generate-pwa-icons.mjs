// One-off / re-runnable generator for the PWA icon set.
// Usage: node scripts/generate-pwa-icons.mjs
import sharp from "sharp";
import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

const BG = "#0f172a"; // slate-900
const FG = "#ffffff";

/** Plain monogram, fills the full canvas — used for regular icons / favicons. */
function monogramSvg(size) {
  const fontSize = Math.round(size * 0.56);
  return `
<svg width="${size}" height="${size}" viewBox="0 0 ${size} ${size}" xmlns="http://www.w3.org/2000/svg">
  <rect width="${size}" height="${size}" rx="${Math.round(size * 0.18)}" fill="${BG}"/>
  <text x="50%" y="54%" text-anchor="middle" dominant-baseline="middle"
        font-family="Arial, Helvetica, sans-serif" font-weight="700"
        font-size="${fontSize}" fill="${FG}">S</text>
</svg>`;
}

/** Maskable icon: keep all content inside the ~80% safe zone so OS masks don't clip it. */
function maskableSvg(size) {
  const fontSize = Math.round(size * 0.42);
  return `
<svg width="${size}" height="${size}" viewBox="0 0 ${size} ${size}" xmlns="http://www.w3.org/2000/svg">
  <rect width="${size}" height="${size}" fill="${BG}"/>
  <text x="50%" y="54%" text-anchor="middle" dominant-baseline="middle"
        font-family="Arial, Helvetica, sans-serif" font-weight="700"
        font-size="${fontSize}" fill="${FG}">S</text>
</svg>`;
}

async function renderPng(svg, outPath, size) {
  await sharp(Buffer.from(svg)).resize(size, size).png().toFile(outPath);
  console.log("wrote", path.relative(root, outPath));
}

async function main() {
  const iconsDir = path.join(root, "public", "icons");
  await mkdir(iconsDir, { recursive: true });

  await renderPng(monogramSvg(192), path.join(iconsDir, "icon-192.png"), 192);
  await renderPng(monogramSvg(512), path.join(iconsDir, "icon-512.png"), 512);
  await renderPng(maskableSvg(512), path.join(iconsDir, "icon-maskable-512.png"), 512);

  // Next.js static metadata file conventions (auto-linked, no manual <link> needed).
  await renderPng(monogramSvg(180), path.join(root, "src", "app", "apple-icon.png"), 180);
  await renderPng(monogramSvg(256), path.join(root, "src", "app", "icon.png"), 256);

  // Favicon fallback.
  await renderPng(monogramSvg(32), path.join(iconsDir, "favicon-32.png"), 32);

  await writeFile(
    path.join(iconsDir, "README.txt"),
    "Generated by scripts/generate-pwa-icons.mjs — re-run after changing the monogram/colors.\n"
  );
}

main();
